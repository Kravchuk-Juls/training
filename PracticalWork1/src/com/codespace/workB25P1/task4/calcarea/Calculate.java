package com.codespace.workB25P1.task4.calcarea;

public class Calculate {
    public static double areaCircle (double circleR) {
        double result = circleR * circleR * Math.PI;
        return result;
    }

    public static double areaSquare (int squareS) {
        double result = squareS * squareS;
        return result;
    }
}
